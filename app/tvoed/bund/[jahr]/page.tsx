interface Props {
  params: { jahr: string };
}

export default function TVoeDJahrPage({ params }: Props) {
  const data = TVOED_BUND_TABELLEN[params.jahr];
  if (!data) {
    return (
      <div className="p-8 text-center">
        <h1 className="text-2xl font-medium text-gray-900 mb-2">Tabelle nicht gefunden</h1>
        <a href="/tvoed/bund" className="text-blue-600">← Zurück zur Übersicht</a>
      </div>
    );
  }
  const statusLabel = data.status === "aktuell" ? "AKTUELL" : data.status === "zukuenftig" ? "ZUKÜNFTIG" : "ARCHIV";
  return (
    <Rechner
      titel={`TVöD Bund ${params.jahr} Gehaltsrechner`}
      untertitel={`${statusLabel} · Gültig ab ${data.gueltigAb}${data.gueltigBis ? ` bis ${data.gueltigBis}` : ""} · ${data.erhoehung}${data.mindest ? ` · ${data.mindest}` : ""}`}
      gueltigAb={data.gueltigAb}
      tabelle={data.tabelle}
      activePath="/tvoed/bund"
      breadcrumb={[
        { label: "TVöD", href: "/tvoed" },
        { label: "TVöD Bund", href: "/tvoed/bund" },
        { label: `${params.jahr} (${statusLabel})`, href: `/tvoed/bund/${params.jahr}` },
      ]}
    />
  );
}

export async function generateStaticParams() {
  return Object.keys(TVOED_BUND_TABELLEN).map(jahr => ({ jahr }));
}
