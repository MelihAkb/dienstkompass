export default function TVoeDVKAJahrPage({ params }: Props) {
  const data = TVOED_VKA_TABELLEN[params.jahr];

  if (!data) {
    return (
      <div className="p-8 text-center">
        <h1 className="text-2xl font-medium text-gray-900 mb-2">Tabelle nicht gefunden</h1>
        <a href="/tvoed/vka" className="text-blue-600">← Zurück zur Übersicht</a>
      </div>
    );
  }

  const statusLabel = data.status === "aktuell" ? "AKTUELL" : data.status === "zukuenftig" ? "ZUKÜNFTIG" : "ARCHIV";
  const statusColor = data.status === "aktuell" ? "bg-green-50 text-green-700" : data.status === "zukuenftig" ? "bg-amber-50 text-amber-700" : "bg-gray-50 text-gray-600";

  return (
    <Rechner
      titel={`TVöD VKA ${params.jahr} Gehaltsrechner`}
      untertitel={`${statusLabel} · Gültig ab ${data.gueltigAb}${data.gueltigBis ? ` bis ${data.gueltigBis}` : ""} · ${data.erhoehung}${data.mindest ? ` · ${data.mindest}` : ""}`}
      gueltigAb={data.gueltigAb}
      tabelle={data.tabelle}
      activePath="/tvoed/vka"
      breadcrumb={[
        { label: "TVöD", href: "/tvoed" },
        { label: "TVöD VKA", href: "/tvoed/vka" },
        { label: `${params.jahr} (${statusLabel})`, href: `/tvoed/vka/${params.jahr}` },
      ]}
    />
  );
}

export async function generateStaticParams() {
  return Object.keys(TVOED_VKA_TABELLEN).map(jahr => ({ jahr }));
}
