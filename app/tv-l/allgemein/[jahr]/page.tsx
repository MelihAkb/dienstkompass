interface Props {
  params: { jahr: string };
}

export default function TVLJahrPage({ params }: Props) {
  const data = TV_L_TABELLEN[params.jahr];

  if (!data) {
    return (
      <div className="p-8 text-center">
        <h1 className="text-2xl font-medium text-gray-900 mb-2">Tabelle nicht gefunden</h1>
        <a href="/tv-l/allgemein" className="text-blue-600">← Zurück zur Übersicht</a>
      </div>
    );
  }

  const statusLabel = data.status === "aktuell" ? "AKTUELL" : data.status === "zukuenftig" ? "ZUKÜNFTIG" : "ARCHIV";

  return (
    <Rechner
      titel={`TV-L ${params.jahr} Gehaltsrechner`}
      untertitel={`${statusLabel} · Gültig ab ${data.gueltigAb}${data.gueltigBis ? ` bis ${data.gueltigBis}` : ""} · ${data.erhoehung}${data.mindest ? ` · ${data.mindest}` : ""}`}
      gueltigAb={data.gueltigAb}
      tabelle={data.tabelle}
      activePath="/tv-l/allgemein"
      breadcrumb={[
        { label: "TV-L", href: "/tv-l" },
        { label: "TV-L allgemein", href: "/tv-l/allgemein" },
        { label: `${params.jahr} (${statusLabel})`, href: `/tv-l/allgemein/${params.jahr}` },
      ]}
    />
  );
}

export async function generateStaticParams() {
  return Object.keys(TV_L_TABELLEN).map(jahr => ({ jahr }));
}
